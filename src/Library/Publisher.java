    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         DefaultTableModel tb1Model = (DefaultTableModel)jTable1.getModel();
         if(tb1Model.getRowCount()==0){
         JOptionPane.showMessageDialog(this, "Table Is Empty");
         }
         else{
             String url = "jdbc:mysql//localhost:3306/library1?useSSL=true";
             String user = "root";
             String pass = "";
             
         try{
         Class.forName("com.mysql.cj.jdbc.Driver");
         Connection con = (Connection) DriverManager.getConnection(url,user,pass);
        
         for(int i=0;i<tb1Model.getRowCount();i++){
         String Id = tb1Model.getValueAt(i, 0).toString();
         String Name= tb1Model.getValueAt(i, 1).toString();
         String Address= tb1Model.getValueAt(i, 2).toString();
         String Number= tb1Model.getValueAt(i, 3).toString();
         
         String quary ="INSERT INTO publisher(id,name,address,telephone) VALUES (?,?,?,?)";
         PreparedStatement pe=(PreparedStatement) con.prepareStatement(quary);
         pe.setString(1,Id);
         pe.setString(2,Name);
         pe.setString(3,Address);
         pe.setString(4,Number);
         
         pe.execute();
         
         }
         JOptionPane.showMessageDialog(this, "data insert successfully");
         tb1Model.setRowCount(0);
         }
         catch(Exception e)
         {
         
         }
                 }
    }//GEN-LAST:event_jButton3ActionPerformed

