    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")||jComboBox1.getSelectedItem().equals("")||jTextField4.getText().equals("")||jTextField5.getText().equals("")){
        JOptionPane.showMessageDialog(this,"Please Enter All Data");
        }
        else{
             String data[]={jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),jComboBox1.getSelectedItem().toString(),jTextField5.getText()};
             DefaultTableModel tb1Model = (DefaultTableModel)jTable1.getModel();
             tb1Model.addRow(data);
             JOptionPane.showMessageDialog(this, "Add Data Successfully");
             jTextField1.setText("");
             jTextField2.setText("");
             jTextField3.setText("");
             jTextField4.setText("");
             jComboBox1.setSelectedItem("");
             jTextField5.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

